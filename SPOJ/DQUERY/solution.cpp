inline int64_t gilbertOrder(int x, int y, int pow, int rotate) {
    if (pow == 0) {
        return 0;
    }
    int hpow = 1 << (pow-1);
    int seg = (x < hpow) ? (
        (y < hpow) ? 0 : 3
    ) : (
        (y < hpow) ? 1 : 2
    );
    seg = (seg + rotate) & 3;
    const int rotateDelta[4] = {3, 0, 0, 1};
    int nx = x & (x ^ hpow), ny = y & (y ^ hpow);
    int nrot = (rotate + rotateDelta[seg]) & 3;
    int64_t subSquareSize = int64_t(1) << (2*pow - 2);
    int64_t ans = seg * subSquareSize;
    int64_t add = gilbertOrder(nx, ny, pow-1, nrot);
    ans += (seg == 1 || seg == 2) ? add : (subSquareSize - add - 1);
    return ans;
}

struct Query{
    int l, r, idx;
    int ord;

    inline void calcOrder() {
        ord = gilbertOrder(l, r, 21, 0);
    }

};

inline bool operator<(const Query &a, const Query &b) {
    return a.ord < b.ord;
}

const int MAXN = 1e6 + 2;
int currVal[MAXN];

void solve(){

    int n;
    cin >> n;
    vi a(n);
    rep(i, n){
        cin >> a[i];
    }

    int q;
    cin >> q;


    vector<Query> queries(q);
    rep(i, q){
        int l, r;
        cin >> l >> r;
        l--; r--;
        queries[i].l = l;
        queries[i].r = r;
        queries[i].idx = i;
        queries[i].calcOrder();
    }

    sort(all(queries));

    vi ans(q);

    set<int> different;
    int curr_l = 0;
    int curr_r = -1;

    for(auto qr : queries){
        while(curr_l > qr.l){
            curr_l--;
            currVal[a[curr_l]]++;
            different.insert(a[curr_l]);
        }
        while(curr_r < qr.r){
            curr_r++;
            currVal[a[curr_r]]++;
            different.insert(a[curr_r]);
        }
        while(curr_l < qr.l){
            currVal[a[curr_l]]--;
            if(currVal[a[curr_l]] == 0){
                different.erase(a[curr_l]);
            }
            curr_l++;
        }
        while(curr_r > qr.r){
            currVal[a[curr_r]]--;
            if(currVal[a[curr_r]] == 0){
                different.erase(a[curr_r]);
            }
            curr_r--;
        }

        ans[qr.idx] = sz(different);
    }

    rep(i, q){
        cout << ans[i] << endl;
    }

}

signed main(){
    fastio;

    solve();

    return 0;
}
